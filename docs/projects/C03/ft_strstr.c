char	*ft_strstr(char *str, char *to_find)
{
	int		i;
	int		j;

	i = 0;
	if (!to_find[0])
		return (str);
	while (str[i])
	{
		j = 0;
		while (str[i + j] == to_find[j])
		{
			if (to_find[j + 1] == '\0')
				return (str + i);
			if (str[i + j + 1] == '\0')
				return ((char *) 0);
			j++;
		}
		i++;
	}
	return ((char *) 0);
}
